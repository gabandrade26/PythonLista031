'''
Elaborar um programa que pergunte quatro valores inteiros e apresente 2 resultados: a) Resultado de suas adições/ b) Resultado de suas multiplicações.
'''

valor_a = int(input("Poderia me informar o valor:"))
valor_b = int(input("Poderia me informar o valor:"))
valor_c = int(input("Poderia me informar o valor:"))
valor_d = int(input("Poderia me informar o valor:"))
adicao = valor_a + valor_b + valor_c + valor_d
print("Aqui é o resultado da adição dos valores:", adicao)
multiplicacao = valor_a * valor_b * valor_c * valor_d
print("E agora o resultado da multiplicação dos valores:", multiplicacao)
