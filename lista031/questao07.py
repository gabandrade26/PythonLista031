'''
Fazer um algoritmo que pergunte 3 númeors e apresente a média aritmética entre estes 3 números.
'''

num1 = float(input("Algoritmo apresente um número:"))
num2 = float(input("Algoritmo apresente um número:"))
num3 = float(input("Algoritmo apresente um número:"))
media = (num1 + num2 + num3) / 3
print("Essa é a média desses números:", media)